# Arithmetic Operators:-
    # To Perform Arithmetic Operation we do use
    # Arithmetic Operators.

x = 7
y = 4

# Addition -->   +
addition = x + y
print(f"Addition: {x} + {y} = {addition}")

# Substraction -->   -
substraction = x - y
print(f"Substraction: {x} - {y} = {substraction}")

# Multiplication -->   *
multiplication = x * y
print(f"Multiplication: {x} * {y} = {multiplication}")

# Division 
    # True Division -->   /
true_division = x / y
print(f"True Division: {x} / {y} = {true_division}")

    # Floor Division -->   //
floor_division = x // y
print(f"Floor Division: {x} // {y} = {floor_division}")

# Power -->   **
power = x ** y
print(f"Power: {x} ** {y} = {power}")

# Mod -->   %
mod = x % y
print(f"Mod: {x} % {y} = {mod}")
